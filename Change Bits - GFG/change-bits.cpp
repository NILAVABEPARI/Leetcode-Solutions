class Solution {
  public:
    vector<int> changeBits(int N) {
        // code here
        int temp=N,cnt=0;
        while(N){
            cnt++;
            N=N>>1;
        }
        N=pow(2,cnt)-1;
        return {N-temp,N};
    }
};

